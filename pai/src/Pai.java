public class Pai {
    protected String nome;
    protected String dataDeNascimento;
    protected String corCabelo;
    protected String corOlhos;
    protected Boolean alcoolismo;

    public Pai(String nome, String dataDeNascimento, String corCabelo, String corOlhos, Boolean alcoolismo) {
        this.nome = nome;
        this.dataDeNascimento = dataDeNascimento;
        this.corCabelo = corCabelo;
        this.corOlhos = corOlhos;
        this.alcoolismo = alcoolismo;
    }
}